    public void logar() {
        String sql = "select * from salaocortesquimicos where login=? and senha =?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtSenha.getText());
            rs = pst.executeQuery();
             
            if(rs.next()){
               String perfil=rs.getString(6);
               //System.out.println(perfil);
               if (perfil.equals("admin")){
               TelaPrincipal principal =  new TelaPrincipal();
               principal.setVisible(true);
               TelaPrincipal.menRelSer.setEnabled(true);
               TelaPrincipal.menCadUsu.setEnabled(true);
               TelaPrincipal.lblUsuario.setText(rs.getString(2));
               TelaPrincipal.lblUsuario.setForeground(Color.yellow);
               
               this.dispose();
            }else{
                TelaPrincipal principal =  new TelaPrincipal();
                principal.setVisible(true);
                TelaPrincipal.lblUsuario.setText(rs.getString(2));
                this.dispose();
            }
                
            }else{
                JOptionPane.showMessageDialog(null,"Usúario e/ou senha inválido(s)");
            }
             
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

